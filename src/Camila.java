public class Camila {
     //declarar variables byte, short, int, double, char, boolean, String, var.
     public static void main(String[] args) {
         // bite, guardan valores pequeños, ocupa un byte de memoria
         byte dia = 5;
         byte grados = -2;

         System.out.println("el dia es: " + dia);
         System.out.println("la temperatura es: " + grados);
         // short, mas grande que byte pero mas pequeño que int

         short año = 2026;
         short ubicacion = -5000;

         System.out.println("el año es: "+ año );
         System.out.println("el ubicacion es: "+ ubicacion );

         //int, para numeros enteros, rango grande

         int peso = 500;
         int edad = 26;

         System.out.println("el peso es: "+ peso);
         System.out.println("la edad es: "+ edad);

         // double, son de tipo decimal

         double altura = 1.70;
         double masa = 60.2;
         System.out.println("la altura es: "+ altura);
         System.out.println("el peso es: "+ masa);

         //char, un valor unico

         char letra = 'A';
         char numero = '5';

         System.out.println("la letra 1 es: "+ letra);
         System.out.println("el numero es: "+ numero );

         //boolean, de tipo logico, pueden tomar 2 valores, verdadero o falso

         boolean estado = true;
         boolean estado2 = false;

         System.out.println("el estado es: "+ estado);
         System.out.println("el estado2 es: "+ estado2);

         // string, para cadenas de caracteres

         String nombre = "camila moreno";
         String telefono = "3208816296";

         System.out.println("el nombre es: "+ nombre);
         System.out.println("el telefono es: "+ telefono);

         // var, java supone el tipo de variable

         var correo = "mcamilaandrea229@gmail.com";
         var edad2 = 3;
         var longitud = 14.5;
         System.out.println("el correo es: "+ correo);
         System.out.println("la edad es: "+ edad2);
         System.out.println("la longitud es: "+ longitud);;


     }


     }


